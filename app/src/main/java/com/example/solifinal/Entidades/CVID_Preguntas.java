package com.example.solifinal.Entidades;

public class CVID_Preguntas {

    private String id;
    private String enunciado;
    private String respuesta_c;
    private String respuesta_p1;
    private String respuesta_p2;
    private String respuesta_p3;
    private String nivel;


    public String getId() { return id; }

    public void setId(String id) { this.id = id; }

    public String getEnunciado() { return enunciado; }

    public void setEnunciado(String enunciado) { this.enunciado = enunciado; }

    public String getRespuesta_c() { return respuesta_c; }

    public void setRespuesta_c(String respuesta_c) { this.respuesta_c = respuesta_c; }

    public String getRespuesta_p1() { return respuesta_p1; }

    public void setRespuesta_p1(String respuesta_p1) { this.respuesta_p1 = respuesta_p1; }

    public String getRespuesta_p2() { return respuesta_p2; }

    public void setRespuesta_p2(String respuesta_p2) { this.respuesta_p2 = respuesta_p2; }

    public String getRespuesta_p3() { return respuesta_p3; }

    public void setRespuesta_p3(String respuesta_p3) { this.respuesta_p3 = respuesta_p3; }

    public String getNivel() { return nivel; }

    public void setNivel(String nivel) { this.nivel = nivel; }
}
